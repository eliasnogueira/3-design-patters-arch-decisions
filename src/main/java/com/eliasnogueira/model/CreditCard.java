package com.eliasnogueira.model;

public class CreditCard {

    private String owner;
    private String number;
    private String ccv;
    private String expirationDateMonth;
    private String expirationDateYear;

    public CreditCard(String owner, String number, String ccv, String expirationDateMonth, String expirationDateYear) {
        this.owner = owner;
        this.number = number;
        this.ccv = ccv;
        this.expirationDateMonth = expirationDateMonth;
        this.expirationDateYear = expirationDateYear;
    }

    public String getOwner() {
        return owner;
    }

    public void setOwner(String owner) {
        this.owner = owner;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getCcv() {
        return ccv;
    }

    public void setCcv(String ccv) {
        this.ccv = ccv;
    }

    public String getExpirationDateMonth() {
        return expirationDateMonth;
    }

    public void setExpirationDateMonth(String expirationDateMonth) {
        this.expirationDateMonth = expirationDateMonth;
    }

    public String getExpirationDateYear() {
        return expirationDateYear;
    }

    public void setExpirationDateYear(String expirationDateYear) {
        this.expirationDateYear = expirationDateYear;
    }

    @Override
    public String toString() {
        return "CreditCard{" +
                "owner='" + owner + '\'' +
                ", number=" + number +
                ", ccv=" + ccv +
                ", expirationDateMonth=" + expirationDateMonth +
                ", expirationDateYear=" + expirationDateYear +
                '}';
    }
}
